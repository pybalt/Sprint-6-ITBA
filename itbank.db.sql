CREATE TABLE IF NOT EXISTS "sucursal" (
	"branch_id"	INTEGER NOT NULL,
	"branch_number"	BLOB NOT NULL,
	"branch_name"	TEXT NOT NULL,
	"branch_address_id"	INTEGER NOT NULL,
	PRIMARY KEY("branch_id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "cliente" (
	"customer_id"	INTEGER NOT NULL,
	"customer_name"	TEXT NOT NULL,
	"customer_surname"	NUMERIC NOT NULL,
	"customer_DNI"	TEXT NOT NULL,
	"dob"	TEXT,
	"branch_id"	INTEGER NOT NULL,
	PRIMARY KEY("customer_id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "cuenta" (
	"account_id"	INTEGER NOT NULL,
	"customer_id"	INTEGER NOT NULL,
	"balance"	INTEGER NOT NULL,
	"iban"	TEXT NOT NULL,
	PRIMARY KEY("account_id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "prestamo" (
	"loan_id"	INTEGER NOT NULL,
	"loan_type"	TEXT NOT NULL,
	"loan_date"	TEXT NOT NULL,
	"loan_total"	INTEGER NOT NULL,
	"customer_id"	INTEGER NOT NULL,
	PRIMARY KEY("loan_id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "tipo_cliente" (
	"tipo_cliente_id"	INTEGER NOT NULL,
	"tipo_cliente_nombre"	TEXT NOT NULL,
	PRIMARY KEY("tipo_cliente_id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "tipo_cuenta" (
	"tipo_cuenta_id"	INTEGER NOT NULL,
	"tipo_cuenta_nombre"	TEXT nor,
	PRIMARY KEY('tipo_cuenta_id' AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "tipo_tarjeta" (
	"tipo_tarjeta_id"	INTEGER NOT NULL,
	"tipo_tarjeta_nombre"	TEXT nor,
	PRIMARY KEY('tipo_tarjeta_id' AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "marca_tarjeta" (
	"marca_tarjeta_id"	INTEGER NOT NULL,
	"marca_tarjeta_nombre"	TEXT nor,
	PRIMARY KEY('marca_tarjeta_id' AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "tarjeta" (
	"tarjeta_id"	INTEGER NOT NULL,
	"tarjeta_numero"	varchar(255),
	"tarjeta_cvv"	varchar(255),
	"tarjeta_fecha_otorgamiento"	varchar(255),
	"tarjeta_fecha_expiracion"	varchar(255),
	PRIMARY KEY("tarjeta_id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "tarjeta_tipo_marca" (
	"tarjeta_tipo_marca_id"	INTEGER NOT NULL,
	"tarjeta_id"	INTEGER NOT NULL,
	"tipo_tarjeta_id"	INTEGER NOT NULL,
	"marca_tarjeta_id"	INTEGER NOT NULL,
	FOREIGN KEY("tarjeta_id") REFERENCES "tarjeta"("tarjeta_id"),
	FOREIGN KEY("tipo_tarjeta_id") REFERENCES "tipo_tarjeta"("tipo_tarjeta_id"),
	FOREIGN KEY("marca_tarjeta_id") REFERENCES "marca_tarjeta"("marca_tarjeta_id"),
	PRIMARY KEY('tarjeta_tipo_marca_id' AUTOINCREMENT)
