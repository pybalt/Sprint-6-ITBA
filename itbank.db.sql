CREATE TABLE IF NOT EXISTS "cliente_cuenta_tipo_tarjeta" (
	"cliente_cuenta_tipo_tarjeta_id"	INTEGER NOT NULL,
	"customer_id"	INTEGER NOT NULL,
	"tipo_cliente_id"	INTEGER NOT NULL,
	"caja_pesos"	INTEGER NOT NULL,
	"caja_dolares"	INTEGER NOT NULL,
	"caja_ahorro"	INTEGER NOT NULL,
	"tarjeta_tipo_marca_id"	INTEGER NOT NULL,
	FOREIGN KEY("tarjeta_tipo_marca_id") REFERENCES "tarjeta_tipo_marca"("tarjeta_tipo_marca_id"),
	FOREIGN KEY("customer_id") REFERENCES "cliente"("customer_id"),
	FOREIGN KEY("tipo_cliente_id") REFERENCES "tipo_cliente"("tipo_cliente_id"),
	PRIMARY KEY("cliente_cuenta_tipo_tarjeta_id" AUTOINCREMENT)
